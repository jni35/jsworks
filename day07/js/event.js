//상세 설명 보기
function showText(){
    //설명 보이기
    document.getElementById("desc").style.display = "block";

    document.getElementById("open").style.display ="none";
}

//상세 설명 닫기
function hidenText(){
    //설명 숨기기
    document.getElementById("desc").style.display = "none";

    document.getElementById("open").style.display ="block";

}

